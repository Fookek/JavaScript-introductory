/*
&& = AND
|| = OR
! = NOT
*/

const temp = 20;

if(temp > 0  && temp <= 30){
    console.log("хорошая погода")
}
else{
    console.log("плохая погода")
}

/*
= assignment operator
== comparison operator
=== strict equality operator (checks value and datatype)
!= inequality operator
!== strict inequality
*/

const PI = "3.14";

if(PI === 3.14){
    console.log("this is PI")
}
else{
    console.log("this is Not Pi")
}